package sakshigothal;

public class DoubleDatatype {
public static void main(String[] args) {
	double db=78.8765d;
	double db1=98.73563d;
	System.out.println("square root of db: "+Math.sqrt(db));
	System.out.println("square root of db1: "+Math.sqrt(db1));
	System.out.println("power of db: "+Math.pow(db,3));
	System.out.println("power of db1: "+Math.pow(db1,4));
	System.out.println("Maximum number: "+Math.min(db, db1));
	System.out.println("Minimum number: "+Math.max(db, db1));
}

}
