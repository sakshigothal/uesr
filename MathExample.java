package sakshigothal;

public class MathExample {
public static void main(String[] args) {
	System.out.println(Math.PI);
	System.out.println(Math.abs(-18)); //it return positive value
	System.out.println(Math.abs(18));
	System.out.println(Math.min(12, 4));// it gives you min value
	System.out.println(Math.max(12, 4));//it gives you max value
	System.out.println(Math.sqrt(64));// it gives you square root
	System.out.println(Math.floor(12.8));
	System.out.println(Math.ceil(12.8));
	System.out.println(Math.round(12.5));
	System.out.println(Math.exp(7));
	System.out.println(Math.pow(12, 3));
	System.out.println(Math.random());// it gives you random value
	System.out.println(Math.random()*10);
	System.out.println(Math.floor(Math.random()*10));
	System.out.println(Math.ceil(Math.max(13.3, 17.3)));
}

}
